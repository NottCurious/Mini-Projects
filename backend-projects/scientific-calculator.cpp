#include <iostream>
#include <math.h>

#define ll long long

ll add(ll a, ll b)
{
  return a + b;
}

ll subtract(ll a, ll b)
{
  return a - b;
}

ll multiply(ll a, ll b)
{
  return a * b;
}

double divide(double a, double b)
{
  return a / b;
}

ll find_remainder(ll a, ll b)
{
  return a % b;
}

double get_sinvalue(int a)
{
  return sin(a);
}

double get_cosvalue(int a)
{
  return cos(a);
}

double get_tanvalue(int a)
{
  return tan(a);
}

double get_asinvalue(int a)
{
  return asin(a);
}

double get_acosvalue(int a)
{
  return acos(a);
}

double get_atanvalue(int a)
{
  return atan(a);
}
